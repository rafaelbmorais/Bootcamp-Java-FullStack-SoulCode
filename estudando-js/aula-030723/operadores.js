const num1 = 5;
const num2 = 8;
const num3 = 4.5;

/*
    Operadores Aritméticos:
    + Adição
    - Subtração
    * Multiplicação
    / Divisão
    ** Exponenciação
    % módulo / resto
*/

let result;

result = num1 + num2;
console.log(result); // 13

result = num1 - num2;
console.log(result); // -3

result = num1 * num2;
console.log(result); // 40

result = num1 / num2;
console.log(result); // 0.625

result = num1 ** num2;
console.log(result); // 390625

result = num2 % num3;
console.log(result); // 3.5 retornou um número real porque priorizou o real que está no divisor